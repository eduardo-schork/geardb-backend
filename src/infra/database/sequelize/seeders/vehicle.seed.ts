export async function seedVehicleModels(sequelize: any) {
    const now = new Date();

    for (const v of vehicles) {
        const vehicleId = uuidv4();
        const label = `${v.brand}_${v.model}_${v.version}_${v.year}`;

        await sequelize.models.VehicleModel.create({
            id: vehicleId,
            brand: v.brand,
            model: v.model,
            version: v.version,
            year: v.year,
            label,
            createdAt: now,
            updatedAt: now,
            deletedAt: null,
        });

        await sequelize.models.VehicleSpecModel.create({
            id: uuidv4(),
            vehicleId,
            ...v.specs,
            createdAt: now,
            updatedAt: now,
            deletedAt: null,
        });
    }

    console.log('üöó Seed de ve√≠culos inseridos com sucesso!');
}
